const num1 = 10;
const num2 = 5;

// Adição
const soma = num1 + num2;
console.log('Soma: ' + soma); // 15

// Subtração
const subtracao = num1 - num2;
console.log('Subtração: ' + subtracao); // 5

// Multiplicação
const multiplicacao = num1 * num2;
console.log('Multiplicação: ' + multiplicacao); // 50

// Divisão
const divisao = num1 / num2;
console.log('Divisão: ' + divisao); // 2

// Módulo
const modulo = num1 % num2;
console.log('Módulo: ' + modulo); // 0

// Exponenciação
const exponenciacao = num1 ** num2;
console.log('Exponenciação: ' + exponenciacao); // 10000000000

// Operadores de incremento e decremento
let contador = 0;
console.log('Contador inicial: ' + contador); // 0
contador++;
console.log('Após incremento: ' + contador); // 1
contador--;
console.log('Após decremento: ' + contador); // 0   

// Operadores de atribuição
let atribuicao = 10;
console.log('Valor inicial: ' + atribuicao); // 10
atribuicao += 5;
console.log('Após += 5: ' + atribuicao); // 15
atribuicao -= 3;
console.log('Após -= 3: ' + atribuicao); // 12
atribuicao *= 2;
console.log('Após *= 2: ' + atribuicao); // 24
atribuicao /= 4;
console.log('Após /= 4: ' + atribuicao); // 6
atribuicao %= 4;
console.log('Após %= 4: ' + atribuicao); // 2   
atribuicao **= 3;
console.log('Após **= 3: ' + atribuicao); // 8

const contaComplexa = num1 + num2 * num1;
console.log('Conta Complexa (num1 + num2 * num1): ' + contaComplexa); // 60

const contaComplexa2 = (num1 + num2) * num1;
console.log('Conta Complexa 2 ((num1 + num2) * num1): ' + contaComplexa2); // 150



